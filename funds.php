					<h2 style="text-align: center;">Risk targeted</h2>
					<p>Risk targeted managed portfolios are portfolios that are always managed to be at the same risk level. ESMA has defined the Synthetic Risk Reward Indicator (SRRI) as the benchmark for all fund managers to risk assess their funds and report that in the Key Investor Information Document (KIID). The SRRI scale is from 1-7. Risk targeted portfolios provide a mean, minimum and maximum drawdown percentage based on two standard deviations which means that 95% of the time the fund will perform within the minimum and maximun drawdowns. </p>
